    public void logaradmin() {
        String sql = "select * from tb_adm where id=? and senha =? ";
        try {
            //as linhas abaixo preparam a consulta ao banco de dados
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtCpfadmin.getText());
            pst.setString(2, txtSenhaadmin.getText());
            // a linha abaixo executa a query
            rs = pst.executeQuery();
            // estrutura de descisão se existir usario e senha correspondente 
            if (rs.next()) {
                telaadmin principal = new telaadmin();
                principal.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "usuario e/ou senha inválido(s)");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }
    
