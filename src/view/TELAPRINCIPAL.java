    private void emitir_aluguel() {
        String sql = "insert into tb_aluguel(quant_alug, status,id_cpf,valor_alug) values (?,?,?,?)";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, bquantidade.getSelectedItem().toString());
            pst.setString(2, bstatus.getText());
            pst.setString(3, id_nome.getText());
            pst.setString(4, bvalor.getText());
             int adicionado = pst.executeUpdate();
            System.out.println(adicionado);
            if (adicionado > 0) {
                JOptionPane.showMessageDialog(null, "Parabéns Aluguel Executado com sucesso foi Cadastrado com Sucesso!!!");
            }else{
                JOptionPane.showMessageDialog(null, "Erro no processo de Aluguel!!!");
           
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }
    
    

