    private void pesquisar_cliente() {

        String sql = "select cpf as CPF,nome as NOME from tb_usuarios where cpf like?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, id_cpf.getText() + "%");
            rs = pst.executeQuery();
            tblusuarios.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void setar_campos() {
        int setar = tblusuarios.getSelectedRow();
        id_nome.setText(tblusuarios.getModel().getValueAt(setar, 1).toString());

    }

