        double valor_fixo = 5.00;
        double resultado = 0;
        int num1 = 1;
        int num2 = 2;
        int num3 = 3;
        int num4 = 4;

        if (bquantidade.getSelectedItem().equals("1")) {
            resultado = num1 * valor_fixo;
        } else if (bquantidade.getSelectedItem().equals("2")) {
            resultado = num2 * valor_fixo;
        } else if (bquantidade.getSelectedItem().equals("3")) {
            resultado = num3 * valor_fixo;
        } else {
            resultado = num4 * valor_fixo;
        }
        
        bvalor.setText(String.valueOf(resultado));
        
